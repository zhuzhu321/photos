package com.liyujian.po;

public class Image {
	private String image_id;
	private String image_name;
	private String image_url;
	private String image_state;
	private String image_uploadtime;
	private String company_id;
	public String getImage_id() {
		return image_id;
	}
	public void setImage_id(String image_id) {
		this.image_id = image_id;
	}
	public String getImage_name() {
		return image_name;
	}
	public void setImage_name(String image_name) {
		this.image_name = image_name;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}
	public String getImage_state() {
		return image_state;
	}
	public void setImage_state(String image_state) {
		this.image_state = image_state;
	}
	public String getImage_uploadtime() {
		return image_uploadtime;
	}
	public void setImage_uploadtime(String image_uploadtime) {
		this.image_uploadtime = image_uploadtime;
	}
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	
}
